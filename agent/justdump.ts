function stalk(pid: number, module: Module) {
    const func_addr: { [key: string]: string } = {};
    let times = 0;

    Stalker.exclude({
        base: Process.getModuleByName('libc.so').base,
        size: Process.getModuleByName('libc.so').size,
    });
    Stalker.follow(pid, {
        events: {
            call: false,
            ret: false,
            exec: false,
            block: false,
            compile: false,
        },
        onReceive: function () {},
        transform: (iterator: any) => {
            let instruction = iterator.next();
            do {
                const name = funcs[instruction.address.sub(module.base)];
                if (name) {
                    logger.info({ tag: 'call' }, `${times}:${name}`);
                    stalk(Process.getCurrentThreadId(), module);
                    times = times + 1;
                }
                iterator.keep();
            } while ((instruction = iterator.next()) !== null);
        },

        onCallSummary: function (summary) {},
    });
}
